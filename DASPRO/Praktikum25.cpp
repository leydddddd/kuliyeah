//Operator Relasional dan Perbandingan
#include <iostream>
using namespace std;

int main()
{
    int Nilai1 = 10, Nilai2 = 2;

    cout<< (Nilai1 == Nilai2)<<endl;
    cout<< (Nilai1 != Nilai2)<<endl;
    cout<< (Nilai1 < Nilai2)<<endl;
    cout<< (Nilai1 <= Nilai2)<<endl;
    cout<< (Nilai1 > Nilai2)<<endl;
    cout<< (Nilai1 >= Nilai2)<<endl;

    return 0;
}
