#include <iostream>

using namespace std;

int main()
{
    int a, b;
    cout<<"Masukkan nilai A = ";cin>>a;
    cout<<"Masukkan nilai B = ";cin>>b;

    cout<<"a + b = "<<(a+b)<<endl;
    cout<<"a - b = "<<(a-b)<<endl;
    cout<<"a * b = "<<(a*b)<<endl;
    cout<<"a / b = "<<(a/b)<<endl;
    cout<<"a % b = "<<(a%b)<<endl;

    return 0;
}
